class Demoifelseif
{
	public static void main(String args[])
	{
		double p=71.22;
		if(p>=70)
		{
			System.out.println("Distinction");
		}
		else if(p>=60)
		{
			System.out.println("First class");
		}
		else if(p>=50)
		{
			System.out.println("Second class");
		}
		else if(p>=40)
		{
			System.out.println("Third class");
		}
		else
		{
			System.out.println("Failed");	
		} 	
	}
}